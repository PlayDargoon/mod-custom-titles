-- Формат таблицы custom_titles соответствует структуре CharTitles.dbc
-- 
-- Параметры:
-- ID                   - Уникальный ID звания (используйте значения > 177, чтобы не конфликтовать со стандартными)
-- condition_ID         - ID условия (можно использовать любое уникальное число)
-- name1-name12         - Названия для разных языков (обычно пустые)
-- name13               - Русское название для МУЖЧИН (ruRU локаль)
-- name14-name16        - Названия для других языков (обычно пустые)
-- name_mask            - Языковая маска (обычно 16712190)
-- name_female1-12      - Названия для женщин на разных языках (обычно пустые)
-- name_female13        - Русское название для ЖЕНЩИН (ruRU локаль)
-- name_female14-16     - Названия для женщин на других языках (обычно пустые)
-- name_female_mask     - Языковая маска для женских названий (обычно 16712490)
-- mask_ID              - ВАЖНО! Битовый индекс в PLAYER__FIELD_KNOWN_TITLES (используйте уникальные значения > 143)
-- required_level       - Минимальный уровень для получения звания (0 = нет требования)
-- required_achievement - ID достижения, необходимого для получения (0 = нет требования)
-- required_item        - ID предмета, необходимого для получения (0 = нет требования)
-- cost                 - Стоимость в золоте (0 = бесплатно)

